#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(char *))
{
	t_list	*current;
	t_list	*previous;

	if (lst)
	{
		current = *lst;
		while (current)
		{
			previous = current;
			current = current->next;
			(*del)(previous->str);
			free(previous);
			previous = 0;
		}
	}
	*lst = 0;
}
